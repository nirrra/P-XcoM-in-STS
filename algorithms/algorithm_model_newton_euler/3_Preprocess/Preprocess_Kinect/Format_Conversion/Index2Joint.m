function joint = Index2Joint(index)
switch index
    case 1
        joint = 'SpineBase';
    case 2
        joint = 'Head';
    case 3
        joint = 'ShoulderLeft';
    case 4
        joint = 'ElbowLeft';
    case 5
        joint = 'WristLeft';
    case 6
        joint = 'HandLeft';
    case 7
        joint = 'ShoulderRight';
    case 8
        joint = 'ElbowRight';
    case 9
        joint = 'WristRight';
    case 10
        joint = 'HandRight';
    case 11
        joint = 'HipLeft';
    case 12
        joint = 'KneeLeft';
    case 13
        joint = 'AnkleLeft';
    case 14
        joint = 'FootLeft';
    case 15
        joint = 'HipRight';
    case 16
        joint = 'KneeRight';
    case 17
        joint = 'AnkleRight';
    case 18
        joint = 'FootRight';
    case 19
        joint = 'SpineShoulder';
end
end

